                        <h2>Doença da Próstata</h2>
                        <p>São tratadas, diagnosticadas e acompanhadas pelos urologistas, que não engloba apenas pênis e testículos, mas também rins, ureteres, bexiga, próstata e vesículas seminais. Porém, os homens evitam procurar por desconhecimento, medo e muitas vezes até pelo preconceito.</p>
                        <p>A próstata é uma glândula presente, exclusivamente, nos homens. Sua função principal é a produção de substâncias envolvidas na formação do líquido ejaculado durante o ato sexual. Até a puberdade, irá atingir seu tamanho normal. Depois dos 30, pode voltar a crescer caso tenha o desenvolvimento da hiperplasia prostática benigna (HPB).</p>
                        <p>Contudo, quando aumenta seu tamanho pode comprimir a uretra. Por esse motivo, pode desencadear uma series de sintomas relacionados a dificuldade de urinar, podendo apresentar: jato urinário fraco, fino e sem pressão; aumento no número de vezes em que vão ao banheiro, devida a sensação de esvaziamento incompleto da bexiga. </p>
                        <p>O câncer de próstata é uma doença que na maioria dos casos são assintomáticas, ou seja, não possuem sintomas. É muito comum após os 50 anos. Os fatores genéticos também são importantes para a ocorrência da doença. Portanto, existem várias maneiras de tratamento; cirurgia, radioterapia e quimioterapia.</p>
                        <p>Outra doença é a prostatite, é inflamatória e pode surgir como um quadro agudo, com febre, mal-estar e dor ao urinar. O tratamento, geralmente, é com antibióticos e anti-inflamatórios receitados pelo urologista.</p>
