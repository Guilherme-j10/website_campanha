                        <h3>- Teste do pezinho</h3>
                        <p>O exame feito a partir de sangue coletado do calcanhar do bebê e que permite identificar doenças graves, como: o hipotireoidismo congênito (incapacidade de produzir quantidades adequadas de hormônios), a fenilcetonúria (doença do metabolismo) e a Anemia Falciforme (doenças que afeta as hemácias).</p>
                        <p>O Teste deve ser realizado em todas as crianças recém-nascidas, a partir de 48 horas de vida até 30 dias do nascimento, isto porque qualquer pessoa pode ter um filho portador da doença, mesmo que nunca tenha aparecido um caso na família. O teste em si apresenta grande importância pois leva a detecção de doenças que não apresentam uma sintomatologia no nascimentos e quando não tratadas cedo, podem causar sérios danos à saúde, inclusive retardo mental irreversível.</p>
