                        <h2 style="margin-top: 40px;">Importância e finalidade do pré-natal na vida das Gestantes </h2>
                        <p>A realização do pré-natal representa papel fundamental em termos de prevenção e/ou detecção precoce de patologias tanto maternas como fetais. Informações sobre as diferentes vivências devem ser trocadas entre as mulheres e os profissionais de saúde, essa possibilidade de intercâmbio de experiências e conhecimentos é considerada a melhor forma de promover a compreensão do processo de gestação, que se dá pelo pré-natal.</p>
                        <p>Os serviços de saúde pelo pré-natal oferecem a gestante calendário de vacinas, exames de rotina, orientações sobre a sua participação nas atividades educativas como reuniões em grupo e visitas domiciliares, o agendamento da consulta médica para pesquisa de fatores de risco, além de esclarecimentos sobre duvidas e medos que cercam a futura mãe.</p>
                        <p>Vantagens do pré-natal: </p>
                        <p>- permite identificar doenças que já estavam presentes no organismo, porém, evoluindo de forma silenciosa, como a hipertensão arterial, diabetes, doenças do coração, anemias, etc;</p>
                        <p>- detecta problemas fetais, como más formações;</p>
                        <p>- avalia aspectos relativos à placenta, que quando inadequada pode provocar graves hemorragias com sérios riscos maternos, por isso a importância da sua detecção para o início imediato do tratamento.</p>
                        <p>Principais objetivos:</p>
                        <p>- preparar a mulher para a maternidade, proporcionando informações educativas sobre o parto e o cuidado da criança (puericultura);</p>
                        <p>- fornecer orientações essenciais sobre hábitos de vida e higiene pré-natal;</p>
                        <p>- orientar sobre a manutenção essencial de estado nutricional apropriado;</p>
                        <p>- orientar sobre o uso de medicações que possam afetar o feto ou o parto ou medidas que possam prejudicar o feto;</p>
                        <p>- fazer prevenção, diagnóstico precoce e tratamento de doenças próprias da gestação ou que sejam intercorrências previsíveis dela;</p>
                        <p>- orientar psicologicamente a gestante para o enfrentamento da maternidade;</p>
