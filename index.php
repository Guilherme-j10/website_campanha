                        <h1 id="crinca">SAÚDE DA CRIANÇA</h1>
                        <h2>EXAMES DE TRIAGEM NEONATAL</h2>
                        <h3>- Teste do olhinho</h3>
                        <p>É um exame simples, rápido e indolor, que consiste na identificação de um reflexo vermelho, que aparece quando um feixe de luz ilumina o olho do bebê. O teste permite constatar qualquer alteração que cause obstrução no eixo visual, como catarata, glaucoma congênito e outros problemas, que diante de sua identificação precoce, possibilita o tratamento imediato para o alcance de uma cura ou em outros casos, controle de agravos.</p>
                        <img src="img/olho.jpeg" alt="">
                        <p>O exame é realizado nas maternidades públicas até a alta do recém-nascido. A recomendação é que o Teste do Olhinho seja feito pelo pediatra logo que o bebê nasce, se isto não ocorrer, o exame deve ser feito logo na primeira consulta de acompanhamento da criança, diante de um teste negativo, a sua realização ainda deve perdurar por toda a sua fase de consultas rotineiras, para a detecção de um desenvolvimento futuro. Já no caso de teste positivo torna-se necessário o encaminhamento da criança para uma avaliação oftalmológica.</p>
                        
                        <h3>- Teste da orelhinha</h3>
                        <img src="img/orelha.jpeg" alt="">
                        <p>O Teste da Orelhinha é um exame importante para detectar se o recém-nascido tem problemas de audição. O teste é caracterizado como rápido, indolor e sem contraindicação, além de ser recomendado a sua realização na maternidade, antes da alta hospitalar, isso garante um diagnóstico e tratamento precoce, ou seja, permite a facilidade do alcance de cura ou em outros casos a diminuição de agravos. Segundo Lei Federal nº 12.303/2010, o teste é obrigatória e gratuito nas unidades de saúde, o que possibilita cada vez mais a garantia de uma vida mais digna e sadia para as futuras gerações.</p>

                        <h3>- Teste Positivo</h3>
                        <p>Quando detectado algum problema, o bebê é encaminhado para um serviço de avaliação otorrinolaringológica e exames complementares. Uma vez confirmado o tipo e o grau da perda auditiva, o bebê será encaminhado para um programa de intervenção precoce a fim de orientar a família, preparar para o uso de aparelhos de amplificação ou implante coclear e terapia fonoaudiológica. </p>
                        <p style="width: 100%;">A surdez tem influência Genética?</p>
                        <p>Segundo conjunto de pesquisas realizadas pelo Ministério e outros Órgãos Competentes foi constatado que a cada 1.000 crianças, uma nasce com problemas de surdez, confirmando-se assim que 60% dos casos são causados pela herança genética do bebê, fato este que reforça a importância da realização do teste da orelhinha, além do acompanhamento pediátrico da criança para um possível desenvolvimento de surdez futuro.</p>
