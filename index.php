                        <h1 id="homem">SAÚDE DO HOMEM</h1>
                        <p>Os homens, desde o seu nascimento, são instruídos que eles são fortes, não choram, não possuem fraquezas e não ficam doentes. Contudo, o homem não cuida da sua saúde e como consequência sofrem por enfermidades que poderiam ter sido evitadas. </p>
                        <img style="width: 100%" src="img/homem2.jpeg" alt="">
                        <p>A Política Nacional de Atenção Integral da Saúde do Homem (PNAISH) promove ações de saúde para a realidade masculina, respeitando os diferentes níveis de desenvolvimento e organização dos sistemas locais de saúde e tipos de gestão de Estados e Municípios.</p>
                        <p>Foram desenvolvidos 5 princípios: </p>
