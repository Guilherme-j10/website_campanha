                        <p>- Exame de Mama</p>  
                        <p>A mamografia é um exame muito importante, pois por meio dele é possível detectar e
                            diagnosticar precocemente o câncer de mama. O exame detecta lesões milimétricas que, pelo
                            exame físico de palpação, não são facilmente identificadas.</p>  
                        <p>A recomendação da Sociedade Brasileira de Mastologia é que as mulheres comecem a realizar
                            o exame de mamografia anualmente a partir dos 40 anos de idade. Para mulheres que estão
                            no grupo de risco, a idade deve ser a partir dos 35. Já as pacientes que possuem idade entre 50
                            e 69 anos, o intervalo máximo deve ser de até 2 anos entre os exames.</p> 
                        <p>Todas as mulheres devem ter consciência de que o câncer de mama é o maior responsável por
                            mortes de mulheres no mundo e, por isso, quanto mais cedo o tumor for descoberto, maiores
                            são as chances de cura.</p>
                        <p>O exame em si não é complicado de se fazer, mas pode gerar certo desconforto pela
                            compressão das mamas, que são sensíveis. No entanto, o médico poderá orientar a paciente
                            quanto à melhor forma de aliviar o desconforto, que nunca deve ser um empecilho para que a
                            mulher, além de fornecer baixo risco radiológico, ou seja, o exame é obtido com raios de baixa
                            energia.</p>
                        <p>Quem faz a mamografia se certifica de não ter câncer ou garante maior chance de cura através
                            da detecção precoce do mesmo. Vale reforçar que somente a mamografia detecta a doença no
                            seu estágio inicial.</p>
