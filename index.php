                        <h1 id="hiv">Combate HIV/AIDS</h1>
                        <img src="img/hiv.jpeg" alt="">
                        <p>HIV ou aids, qual a diferenças ?</p>
                        <p>HIV e aids não são sinônimos. HIV (Vírus da Imunodeficiência Humana) é o vírus causador da aids, que ataca células específicas do sistema imunológico (os linfócitos T-CD4+), responsáveis por defender o organismo contra doenças. Ao contrário de outros vírus, como o da gripe, o corpo humano não consegue se livrar do HIV. Ter HIV não significa que a pessoa desenvolverá aids; porém, uma vez infectada, a pessoa viverá com o HIV durante toda sua vida. Não existe vacina ou cura para infecção pelo HIV, mas há tratamento.</p>
                        <p>Aids (Síndrome da Imunodeficiência Adquirida) é a doença causada pelo HIV, que ataca células específicas do sistema imunológico, responsáveis por defender o organismo de doenças. Em um estágio avançado da infecção pelo HIV, a pessoa pode apresentar diversos sinais e sintomas, além de infecções oportunistas (pneumonias atípicas, infecções fúngicas e parasitárias) e alguns tipos de câncer. Sem o tratamento antirretroviral, o HIV usa essas células do sistema imunológico para replicar outros vírus e as destroem, tornando o organismo incapaz de lutar contra outras infecções e doenças.</p>
                        <p>A transmissão do HIV se dá por meio de relações sexuais desprotegidas, pelo compartilhamento de seringas, materiais perfuro cortantes contaminados e não esterilizados e por meio da transmissão vertical durante a gravidez, parto e/ou amamentação, quando não tomadas as devidas medidas de prevenção, sendo elas: uso correto da camisinha em todas as relações sexuais, apenas o uso de agulhas e seringas descartáveis, para evitar que a aids passe da mãe para o filho, todas as gestantes devem começar o pré-natal o mais cedo possível e fazer o teste de aids, que quando confirmado torna-se necessário a utilização de medicamentos para que não ocorre a passagem do vírus para o feto. Deve-se ressaltar em primeira instancia a importância e necessidade da realização regular de exames para AIDS/HIV, principalmente por parte dos jovens sexualmente ativos.</p>
                        <p>Outro cuidado importante, é com as doenças sexualmente transmissíveis, as DST, elas podem trazer sérios problemas de saúde, além de aumentar em até 18 vezes a chance de contrair o HIV, ou seja, a prevenção as DST é igual a da AIDS/HIV.</p>
