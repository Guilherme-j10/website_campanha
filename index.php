                        é incomum que homens em fase de envelhecimento apresentem queixas
                        clínicas diretamente relacionadas ao avançar da idade,
                        manifestações que trazem desconforto e diminuem a qualidade de vida.
                        Cansaço, falta de concentração, esquecimento, perda de força muscular,
                        disfunção erétil, perda da libido, entre outras, podem estar relacionadas ao
                        Distúrbio Androgênico do Envelhecimento Masculino (DAEM), popularmente
                        chamado de “andropausa” ou “menopausa masculina”.
                        </p>
                        <p>A DAEM é uma das características do envelhecimento do homem e se
                        relaciona à diminuição da testosterona, o hormônio sexual masculino. Ela
                        é produzida nos testículos através de células estimuladas por hormônios da
                        hipófise, uma glândula localizada no cérebro.
                        </p>
