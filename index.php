                        <h1 id="mulher">SAÚDE DA MULHER</h1>
                        <img src="img/WhatsApp Image 2020-05-24 at 19.15.42.jpeg" alt="asd">
                        <h2>Exames de rastreamento</h2>
                        <p>São exames utilizados para detectar uma doença em pessoas assintomáticas. A ideia central do
                            rastreamento é que a doença seja identificada antes dos sintomas, possibilitando começar o
                            tratamento nas fases iniciais da doença com objetivo de aumentar as chances de cura. Diante
                            do cuidado e relevância do bem estar da mulher, os dois exames mais incentivados de serem
                            realizados são, exame de Papanicolau e exame de mama, cujos canceres tem apresentado um
                            número crescente de casos.</p>
                        <p>- Exame Papanicolau</p>
                        <p>O Papanicolau é um exame muito importante, que tem o objetivo de fazer o diagnóstico
                            precoce das lesões precursoras do câncer do colo do útero. Também chamado de preventivo,
                            ele deve ser realizado anualmente a partir dos 25 anos de idade para as mulheres que já
                            tiveram atividade sexual e seguir até os 64 anos, após essa idade, o exame pode ser
                            interrompido mediante a apresentação de pelo menos dois exames negativos consecutivos,
                            nos últimos cinco anos.</p>
                        <p>Primeiramente, o ginecologista faz um exame externo da vulva. Em seguida, introduz o
                            espéculo, também conhecido como bico de pato, pelo canal vaginal para permitir a
                            visualização do colo do útero. Ele coleta material da vagina e do colo uterino e envia para ser
                            examinado em laboratório por um patologista.</p>
                        <p>O exame genital periódico também permite detectar infecções viróticas, por exemplo,
                            verrugas genitais e herpes, além de alterações hormonais, principalmente de progesterona e
                            estrogênio.</p>
